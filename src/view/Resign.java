    public Resign() {
        initComponents();
        
        //ADD DEPARTEMENT & LEVEL COMBO BOX
        employee.removeAllItems(); 

        String sqllevel = "SELECT idemployee,employeename from employee where isdeleted=0";

        try {
            PreparedStatement ps2 = conn.prepareStatement(sqllevel);
            ResultSet rs2 = ps2.executeQuery();
            
            while (rs2.next()) {
                int idlevel = rs2.getInt("idemployee");
                String namelevel = rs2.getString("employeename");
                employee.addItem(new ComboItemResign(idlevel, namelevel).toString()); 
            }
            
            rs2.close();
            ps2.close();

        } catch (Exception e) {
            e.printStackTrace();
        }

        tblResign.setModel(resigntablemodel);
        loadData();
    }

