       private void dataTabel() {
//        dataUser.setVisible(false);
//        addUser.setVisible(true);

        int row = table_attendance_limit.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(null, "Please select.");
            return;
        }

//        jLabel2.setText("Perbarui Data Karyawan");
        header_new_attendance_limit.setText("Edit Attendance Limit");

        int thresholdid = (int) table_attendance_limit.getModel().getValueAt(row, 0);
        AttendanceLimitModel thresholdmodel = attendancelimitservice.getById(thresholdid);

        if (thresholdmodel == null) {
            JOptionPane.showMessageDialog(null, "Attendance limit data not found.");
            return;
        }
        
        String getiddept = "SELECT deptname FROM departement WHERE iddept=? AND isdeleted=0";
        
         try {
            PreparedStatement psiddept = conn.prepareStatement(getiddept);
            psiddept.setInt(1, thresholdmodel.getIdDept());
            ResultSet rsIdDept = psiddept.executeQuery();

            String deptname = null;
            if (rsIdDept.next()) {
                deptname = rsIdDept.getString("deptname");
                
                // Set nilai ke input form
                waktu_mulai_absensi_field.setText(thresholdmodel.getStarttimestr());
                attendance_dropdown.setSelectedItem(thresholdmodel.getAttendancename());
                batas_tepat_waktu_field.setText(thresholdmodel.getEndtime());
                waktu_akhir_jam_absensi_field.setText(thresholdmodel.getAttendanceLimittime());
                departement_dropdown.setSelectedItem(deptname);
                
            } else {
                JOptionPane.showMessageDialog(null, "Departement not found!");
                return;
            }

        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, 
                "Failed to proccess data departemenr:\n" + e.getMessage());
        }

//        active();
        add_on_modal.setText("Update");
        cancel_button.setVisible(true);
    }
       
