    private void createData() {
        String getstarttime = waktu_mulai_absensi_field.getText();
        String getendtime = batas_tepat_waktu_field.getText();
        String getthreshold = waktu_akhir_jam_absensi_field.getText();
        String getdepartemen = departement_dropdown.getSelectedItem().toString();
        String getattendancename = attendance_dropdown.getSelectedItem().toString();
        String getiddept = "SELECT iddept FROM departement WHERE deptname=? AND isdeleted=0";
         String checkdata = "SELECT iddeptthreshold FROM threshold WHERE iddeptthreshold=? AND attendancename=? AND isdeleted=0";
        try {
            PreparedStatement psiddept = conn.prepareStatement(getiddept);
            psiddept.setString(1, getdepartemen);
            ResultSet rsIdDept = psiddept.executeQuery();
            
            Integer iddept = null;
            if (rsIdDept.next()) {
                iddept = rsIdDept.getInt("iddept");
                 PreparedStatement checkdataid = conn.prepareStatement(checkdata);
                 checkdataid.setInt(1, iddept);
                 checkdataid.setString(2, getattendancename);
                 ResultSet rsCheckData = checkdataid.executeQuery();
                 if(rsCheckData.next()){
                    JOptionPane.showMessageDialog(null, "Attendance limit for departement "+getdepartemen+" already registered");
                    return; 
                 }
            } else {
                JOptionPane.showMessageDialog(null, "Departement not found!");
                return;
            }

            AttendanceLimitModel thresholdmodel = new AttendanceLimitModel();
            thresholdmodel.setAttendancename(getattendancename);
            thresholdmodel.setIdDept(iddept);
            thresholdmodel.setStarttimestr(getstarttime);
            thresholdmodel.setEndtime(getendtime);
            thresholdmodel.setAttendanceLimittime(getthreshold);

            attendancelimitservice.addAttendanceLimit(thresholdmodel);
            attendancelimittablemodel.addAttendanceLimit(thresholdmodel);

            loadData();
            add_dialog.dispose();
            resetForm();
//            showPanel();
            add_on_modal.setText("Add");

        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, 
                "Failed to proccess attendance limit:\n" + e.getMessage());
        }
    }


    private void updateData() {
        int row = table_attendance_limit.getSelectedRow();
        if (row != -1) {
            int thresholdid = (int) table_attendance_limit.getModel().getValueAt(row, 0);
            String getiddept = "SELECT iddept FROM departement WHERE deptname=? AND isdeleted=0";
            String checkdata = "SELECT iddeptthreshold,idthreshold FROM threshold WHERE iddeptthreshold=? AND attendancename=? AND isdeleted=0";
            String getstarttime = waktu_mulai_absensi_field.getText();
            String getendtime = batas_tepat_waktu_field.getText();
            String getthreshold = waktu_akhir_jam_absensi_field.getText();
            String getattendancename = attendance_dropdown.getSelectedItem().toString();
            String getdepartemen = departement_dropdown.getSelectedItem().toString();

            try {
                PreparedStatement psiddept = conn.prepareStatement(getiddept);
                psiddept.setString(1, getdepartemen);
                ResultSet rsIdDept = psiddept.executeQuery();

                Integer iddept = null;
                if (rsIdDept.next()) {
                    iddept = rsIdDept.getInt("iddept");
                     PreparedStatement checkdataid = conn.prepareStatement(checkdata);
                     checkdataid.setInt(1, iddept);
                     checkdataid.setString(2, getattendancename);
                     ResultSet rsCheckData = checkdataid.executeQuery();
                     if(rsCheckData.next()){
                         if(rsCheckData.getInt("idthreshold")!= thresholdid){
                            JOptionPane.showMessageDialog(null, "Attendance limit for departement "+getdepartemen+" already registered");
                            return; 
                         }
                     }
                } else {
                    JOptionPane.showMessageDialog(null, "Departement not found!");
                    return;
                }

                AttendanceLimitModel thresholdmodel = new AttendanceLimitModel();
                thresholdmodel.setId(thresholdid);
                thresholdmodel.setIdDept(iddept);
                thresholdmodel.setAttendancename(getattendancename);
                thresholdmodel.setStarttimestr(getstarttime);
                thresholdmodel.setEndtime(getendtime);
                thresholdmodel.setAttendanceLimittime(getthreshold);

                attendancelimitservice.editAttendanceLimit(thresholdmodel);
                attendancelimittablemodel.editAttendanceLimit(row, thresholdmodel);

                loadData();
                resetForm();
                add_dialog.dispose();
//                showPanel();

            } catch (SQLException e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(null,
                    "Failed to proccess attendance limit:\n" + e.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please select!");
        }
    }

    private void deleteData() {
        int index = table_attendance_limit.getSelectedRow();
        if (index != -1) {
            int thresholdid = (int) table_attendance_limit.getModel().getValueAt(index, 0);
            AttendanceLimitModel getAttendanceLimit = attendancelimitservice.getById(thresholdid);

            AttendanceLimitModel thresholdmodel = getAttendanceLimit;

            if (JOptionPane.showConfirmDialog(null, "Are you sure, you want delete this data?", "Confirm", JOptionPane.OK_CANCEL_OPTION) == JOptionPane.OK_OPTION) {
                attendancelimitservice.deleteAttendanceLimit(thresholdmodel);
                attendancelimittablemodel.deleteAttendanceLimit(index);
                loadData();
                resetForm();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please select");
        }
    }

