    private void dataTabel() {
//        dataClasses.setVisible(false);
//        addClasses.setVisible(true);
        
        employee_dropdown.setEnabled(false);

        int row = table_overtime.getSelectedRow();

        int overtimeid = (int) table_overtime.getModel().getValueAt(row, 0);
        OvertimeModel overtimemodel = overtimeservice.getById(overtimeid);
        
        String getemployeename = "SELECT employeename FROM employee WHERE employeename = ? AND isdeleted = 0";
        
        try {
            PreparedStatement psemployeename = conn.prepareStatement(getemployeename);
            psemployeename.setString(1, overtimemodel.getEmployeeName());
            ResultSet rsIdEmployee = psemployeename.executeQuery();

            String employeenamedata = null;
            if (rsIdEmployee.next()) {
                employeenamedata = rsIdEmployee.getString("employeename");
                
                // Set nilai ke input form
                employee_dropdown.setSelectedItem(employeenamedata);
                in_time_picker.setText(overtimemodel.getStartTime());
                out_time_picker.setText(overtimemodel.getEndTime());
                reason_field.setText(overtimemodel.getOvertimeReason());
                header_new_overtime.setText("Edit Overtime");
//                jLabel2.setText("Perbarui Data Lembur Karyawan");
                
            } else {
                JOptionPane.showMessageDialog(null, "Employee tidak ditemukan di database!");
                return;
            }

        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, 
                "Terjadi kesalahan saat memproses data batas Employee:\n" + e.getMessage());
        }

//        active();
//        add_on_modal.setText("Update");
//        btnBatalTambah.setVisible(true);
    }
    
