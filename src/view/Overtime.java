                                            "date(o.created_at) as overtimedate  from overtime as o\n" +
                                            "left join employee as e on o.idemployeeovertime = e.idemployee WHERE o.isdeleted = 0 AND DATE(o.created_at) = ? AND e.employeename != ?";                    
                    PreparedStatement psDouble = conn.prepareStatement(checkingdoubledata);
