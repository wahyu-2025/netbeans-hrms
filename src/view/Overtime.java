   private void createData() {
        if (validasiInput()) {
            String getemployeename = (String) employee_dropdown.getSelectedItem();
            String startText = in_time_picker.getText();
            String endText = out_time_picker.getText();
            
            try {
                // Ambil tanggal sekarang (tanpa waktu)
                LocalDate today = LocalDate.now();
                String checkingdoubledata = "select o.idovertime,o.idemployeeovertime,e.employeename,o.overtimereason,o.starttime,o.endtime,o.duration,\n" +
                                            "date(o.created_at) as overtimedate  from overtime as o\n" +
                                            "left join employee as e on o.idemployeeovertime = e.idemployee WHERE o.isdeleted = 0 AND DATE(o.created_at) = ?";
                PreparedStatement psDouble = conn.prepareStatement(checkingdoubledata);
                psDouble.setDate(1, java.sql.Date.valueOf(today)); 

                ResultSet rsdouble = psDouble.executeQuery();

                if (rsdouble.next()) {
                    String existingName = rsdouble.getString("employeename");
                    if(existingName.equalsIgnoreCase(getemployeename)){
                        JOptionPane.showMessageDialog(null, "Overtime data for " + existingName + " is available today.");
                        rsdouble.close();
                        psDouble.close();
                        return;
                    }
                }

                rsdouble.close();
                psDouble.close();

            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(null, "Gagal memeriksa data lembur ganda.");
                return;
            }

            // Formatter untuk 12 jam dan 24 jam
            DateTimeFormatter formatter12Hour = DateTimeFormatter.ofPattern("h:mma");
            DateTimeFormatter formatter24Hour = DateTimeFormatter.ofPattern("H:mm");

            LocalTime start = null;
            LocalTime end = null;

            try {
                start = LocalTime.parse(startText.toUpperCase(), formatter12Hour);
            } catch (Exception e) {
                start = LocalTime.parse(startText, formatter24Hour);
            }

            try {
                end = LocalTime.parse(endText.toUpperCase(), formatter12Hour);
            } catch (Exception e) {
                end = LocalTime.parse(endText, formatter24Hour);
            }

            long durationInMinutes = ChronoUnit.MINUTES.between(start, end);
            if (durationInMinutes < 0) {
                durationInMinutes += 1440; // Jika end < start, tambahkan 24 jam
            }

            // Validasi minimal 1 jam
            if (durationInMinutes < 60) {
                JOptionPane.showMessageDialog(null, "The duration of overtime must not be less than 1 hour.");
                return;
            }

            Integer deptname = null;
            String endTimeStr = "";

            try {
                // Ambil deptname berdasarkan employeename
                String sqlDept = "SELECT iddeptemployee FROM employee WHERE employeename = ? and isdeleted=0";
                PreparedStatement psDept = conn.prepareStatement(sqlDept);
                psDept.setString(1, getemployeename);
                ResultSet rsDept = psDept.executeQuery();

                if (rsDept.next()) {
                    deptname = rsDept.getInt("iddeptemployee");
                } else {
                    JOptionPane.showMessageDialog(null, "Departemen tidak ditemukan untuk karyawan ini.");
                    rsDept.close();
                    psDept.close();
                    return;
                }

                rsDept.close();
                psDept.close();

                // Ambil endtimestr dari threshold
                String sqlThreshold = "SELECT endtimestr FROM threshold WHERE iddeptthreshold = ? AND attendancename = 'OUT' and isdeleted = 0";
                PreparedStatement psThresh = conn.prepareStatement(sqlThreshold);
                psThresh.setInt(1, deptname);
                ResultSet rsThresh = psThresh.executeQuery();
                
                if (rsThresh.next()) {
                    endTimeStr = rsThresh.getString("endtimestr");
                } else {
                    JOptionPane.showMessageDialog(null, "Threshold untuk departemen ini tidak ditemukan.");
                    rsThresh.close();
                    psThresh.close();
                    return;
                }
                
                rsThresh.close();
                psThresh.close();

                // Parse endTimeStr dari threshold ke LocalTime
                LocalTime thresholdEnd = LocalTime.parse(endTimeStr.toUpperCase(), formatter12Hour);

                // Validasi: end tidak boleh lebih awal dari thresholdEnd
                if (end.isBefore(thresholdEnd)) {
                    JOptionPane.showMessageDialog(null, "Jam selesai lembur tidak boleh lebih awal dari waktu OUT (" + endTimeStr + ").");
                    return;
                }

            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(null, "Terjadi kesalahan saat mengambil data departemen atau threshold.");
                return;
            }

            // Simpan data ke model
            
            try {
                String getidemployee = "SELECT idemployee FROM employee where isdeleted = 0 and employeename = ?";
                PreparedStatement idemployee = conn.prepareStatement(getidemployee);
                idemployee.setString(1, getemployeename); 

                ResultSet rsgetidemployee = idemployee.executeQuery();

                if (rsgetidemployee.next()) {
                    OvertimeModel overtimemodel = new OvertimeModel();
                    overtimemodel.setIdEmployee(rsgetidemployee.getInt("idemployee"));
                    overtimemodel.setStartTime(startText);
                    overtimemodel.setEndTime(endText);
                    overtimemodel.setOvertimeReason(reason_field.getText());
                    overtimemodel.setDuration((int) durationInMinutes);

                    overtimeservice.addOvertime(overtimemodel);
                    overtimetablemodel.addOvertime(overtimemodel);
                    
                }

            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(null, "Gagal mendapatkan data ID karyawan.");
                return;
            }
            loadData();
            resetForm();
            add_dialog.dispose();
//            showPanel();
            add_on_modal.setText("Add");
        }
    }

