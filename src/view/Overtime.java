    private void deleteData() {
        int index = table_overtime.getSelectedRow();
        if (index != -1) {
            int deptId = (int) table_overtime.getModel().getValueAt(index, 0);
            OvertimeModel getovertime = overtimeservice.getById(deptId);

            OvertimeModel overtimemodel = getovertime;

            if (JOptionPane.showConfirmDialog(null, "Are you sure, you want delete this data?", "Confirm", JOptionPane.OK_CANCEL_OPTION) == JOptionPane.OK_OPTION) {
                overtimeservice.deleteOvertime(overtimemodel);
                overtimetablemodel.deleteOvertime(index);
                loadData();
                resetForm();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please select");
        }
    }

